    public function profile_update(Request $request, $id)
    {
        $user = User::find($id);
        $data = [
            'name' => $request->name,
        ];
        if ($request->password) {
            $data['password'] = Hash::make($request->password);
        }
        $user->update($data);
        $alert = 'Profile has been updated!';
        return redirect('/admin/profile')->with('success', $alert);
    }

